from mcp.server.fastmcp import FastMCP

mcp = FastMCP("PizzaAssistant")

@mcp.tool()
def get_pizza_ingredients(pizza_name: str) -> str:
    """Узнать точный состав пиццы по её названию."""
    ingredients = {
        "пицца маргарита": "Томатный соус, моцарелла, базилик, оливковое масло",
        "пицца пепперони": "Томатный соус, моцарелла, пепперони",
        "пицца 4 сыра": "Моцарелла, горгонзола, пармезан, эмменталь",
        "пицца гавайская": "Томатный соус, моцарелла, ветчина, ананасы",
        "пицца вегетарианская": "Томатный соус, моцарелла, грибы, перец, томаты, оливки"
    }
    return ingredients.get(pizza_name.lower(), "Извините, такой пиццы нет в нашем меню.")

@mcp.tool()
def check_delivery_time(district: str) -> str:
    """Проверить примерное время доставки в указанный район города."""
    times = {
        "центр": "20-30 минут",
        "спальный район": "30-45 минут",
        "пригород": "45-60 минут"
    }
    return times.get(district.lower(), "Для уточнения времени доставки свяжитесь с оператором.")

if __name__ == "__main__":
    mcp.run(transport="stdio")